def evaluate_single(
    client: Anthropic,
    query: str,
    response: str,
    rubric: Dict[str, Any]
) -> Dict[str, Any]:
    """
    Evaluate a single query-response pair using LLM-as-judge.

    Returns:
        Dictionary with scores and reasoning for each category
    """

    judge_prompt = create_judge_prompt(query, response, rubric)

    try:
        # Call judge with temperature=0 for deterministic scoring
        judge_response = client.messages.create(
            model=JUDGE_MODEL,
            temperature=JUDGE_TEMPERATURE,
            max_tokens=JUDGE_MAX_TOKENS,
            messages=[
                {"role": "user", "content": judge_prompt}
            ]
        )

        judge_text = judge_response.content[0].text

        # Extract JSON from response
        scores = parse_judge_response(judge_text, rubric)

        return {
            "scores": scores,
            "judge_reasoning": judge_text,
            "success": True
        }

    except Exception as e:
        print(f"⚠️ Error evaluating case: {e}")
        return {
            "scores": {cat: {"score": 0, "reasoning": f"Error: {e}"} for cat in rubric.keys()},
            "judge_reasoning": f"Error during evaluation: {e}",
            "success": False
        }

def parse_judge_response(judge_text: str, rubric: Dict[str, Any]) -> Dict[str, Dict[str, Any]]:
    """
    Parse judge response to extract scores.

    Tries JSON parsing first, falls back to text parsing.
    """

    # Try to extract JSON block
    try:
        # Look for ```json ... ``` block
        if "```json" in judge_text:
            start = judge_text.find("```json") + 7
            end = judge_text.find("```", start)
            json_str = judge_text[start:end].strip()
            return json.loads(json_str)

        # Try parsing entire response as JSON
        return json.loads(judge_text)

    except json.JSONDecodeError:
        # Fallback: text parsing
        return parse_scores_from_text(judge_text, rubric)

def parse_scores_from_text(text: str, rubric: Dict[str, Any]) -> Dict[str, Dict[str, Any]]:
    """
    Fallback parser when JSON extraction fails.

    Looks for patterns like "category: X/5" or "score: X"
    """
    import re

    scores = {}

    for category in rubric.keys():
        # Look for "category: X/5" or "category: X"
        patterns = [
            rf"{category}.*?(\d)/5",
            rf"{category}.*?score.*?(\d)",
            rf"\*\*{category}\*\*.*?(\d)"
        ]

        score = None
        for pattern in patterns:
            match = re.search(pattern, text, re.IGNORECASE | re.DOTALL)
            if match:
                score = int(match.group(1))
                break

        if score is None:
            score = 0  # Default if parsing fails

        scores[category] = {
            "score": score,
            "reasoning": "Extracted from text (JSON parsing failed)"
        }

    return scores

