def judge_response(query, response, rubric):
    """Evaluate a single query-response pair."""

    api_key = os.getenv("ANTHROPIC_API_KEY")
    if not api_key:
        print("❌ ANTHROPIC_API_KEY not found. Set it in .env file.")
        return None

    client = Anthropic(api_key=api_key)
    judge_prompt = create_judge_prompt(query, response, rubric)

    # Call judge with temperature=0
    judge_response = client.messages.create(
        model=JUDGE_MODEL,
        temperature=JUDGE_TEMPERATURE,
        max_tokens=2048,
        messages=[{"role": "user", "content": judge_prompt}]
    )

    judge_text = judge_response.content[0].text

    # Parse JSON response
    try:
        # Extract JSON block if wrapped in ```
        if "```json" in judge_text:
            start = judge_text.find("```json") + 7
            end = judge_text.find("```", start)
            json_str = judge_text[start:end].strip()
            scores = json.loads(json_str)
        else:
            scores = json.loads(judge_text)

        return scores
    except json.JSONDecodeError:
        print(f"⚠️ Failed to parse judge response")
        return None

def evaluate_dataset(dataset_path, rubric):
    """Evaluate entire dataset."""

    # Load dataset
    df = pd.read_csv(dataset_path)
    print(f"\n📊 Evaluating {len(df)} test cases...")
    print(f"Judge Model: {JUDGE_MODEL} (temperature={JUDGE_TEMPERATURE})\n")

    results = []

    for idx, row in df.iterrows():
        print(f"  [{idx+1}/{len(df)}] Evaluating...", end="\r")

        scores = judge_response(row['query'], row['response'], rubric)

        if scores:
            results.append({
                "query": row['query'],
                "response": row['response'],
                "scores": scores,
                "label": row.get('label', None)
            })

    print(f"\n✅ Evaluation complete!\n")

    # Calculate aggregate metrics
    category_means = {cat: [] for cat in rubric.keys()}

    for result in results:
        for category, details in result['scores'].items():
            if category in category_means:
                category_means[category].append(details['score'])

    print("📈 Results Summary:")
    for category, scores in category_means.items():
        if scores:
            mean_score = sum(scores) / len(scores)
            threshold = rubric[category]['pass_threshold']
            status = "✅" if mean_score >= threshold else "⚠️"
            print(f"   {status} {category.title()}: {mean_score:.1f}/5.0 (threshold: ≥{threshold})")

    # Save results
    with open("eval_results.json", "w") as f:
        json.dump(results, f, indent=2)

    print(f"\n💾 Results saved to: eval_results.json")

    return results

