    print(f"Project: {PROJECT_NAME}")
    print(f"Dataset: {dataset_path}")
    print(f"Judge Model: {JUDGE_MODEL} (temperature={JUDGE_TEMPERATURE})")
    print(f"Categories: {', '.join(rubric.keys())}\n")

    # Load dataset
    try:
        df = pd.read_csv(dataset_path)
        required_cols = ['query', 'response']
        if not all(col in df.columns for col in required_cols):
            print(f"❌ Dataset must have columns: {required_cols}")
            print(f"Found columns: {list(df.columns)}")
            sys.exit(1)

    except Exception as e:
        print(f"❌ Error loading dataset: {e}")
        sys.exit(1)

    print(f"✅ Loaded {len(df)} test cases\n")

    # Initialize client
    client = get_anthropic_client()

    # Evaluate each case
    results = []
    print("Evaluating test cases...")

    for idx, row in df.iterrows():
        print(f"  [{idx+1}/{len(df)}] Evaluating...", end="\r")

        eval_result = evaluate_single(
            client=client,
            query=row['query'],
            response=row['response'],
            rubric=rubric
        )

        results.append({
            "test_case_id": idx,
            "query": row['query'],
            "response": row['response'],
            "scores": eval_result['scores'],
            "judge_reasoning": eval_result['judge_reasoning'],
            "success": eval_result['success']
        })

    print(f"\n✅ Evaluation complete!\n")

    # Aggregate results
    aggregate_metrics = aggregate_results(results, rubric)

    # Save results
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    run_dir = Path(output_dir) / f"run_{timestamp}"
    run_dir.mkdir(parents=True, exist_ok=True)

    save_results(results, aggregate_metrics, run_dir, rubric)

    return {
        "results": results,
        "metrics": aggregate_metrics,
        "run_dir": str(run_dir)
    }

