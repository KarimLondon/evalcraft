def aggregate_results(results: List[Dict[str, Any]], rubric: Dict[str, Any]) -> Dict[str, Any]:
    """
    Calculate aggregate metrics across all test cases.

    Best practice: Don't optimize for high pass rates - a 70% pass rate
    might indicate more meaningful evaluation than 100%.
    """

    category_scores = {cat: [] for cat in rubric.keys()}
    pass_counts = {cat: 0 for cat in rubric.keys()}
    overall_pass_count = 0

    for result in results:
        if not result['success']:
            continue

        # Collect per-category scores
        for category, details in result['scores'].items():
            if category in category_scores:
                score = details['score']
                category_scores[category].append(score)

                # Check if passes threshold
                if score >= rubric[category]['pass_threshold']:
                    pass_counts[category] += 1

        # Check overall pass
        if check_overall_pass(result['scores'], rubric):
            overall_pass_count += 1

    # Calculate statistics
    num_cases = len([r for r in results if r['success']])

    metrics = {
        "total_cases": num_cases,
        "overall_pass_count": overall_pass_count,
        "overall_pass_rate": overall_pass_count / num_cases if num_cases > 0 else 0,
        "categories": {}
    }

    for category, scores in category_scores.items():
        if len(scores) > 0:
            import statistics
            metrics["categories"][category] = {
                "mean": statistics.mean(scores),
                "median": statistics.median(scores),
                "stdev": statistics.stdev(scores) if len(scores) > 1 else 0,
                "min": min(scores),
                "max": max(scores),
                "pass_count": pass_counts[category],
                "pass_rate": pass_counts[category] / len(scores),
                "pass_threshold": rubric[category]['pass_threshold']
            }

    return metrics

def check_overall_pass(scores: Dict[str, Dict[str, Any]], rubric: Dict[str, Any]) -> bool:
    """
    Determine if this case passes overall evaluation.

    Uses logic defined in RUBRIC configuration:
    - ALL_MUST_PASS: Every category must meet its threshold
    - Weighted average: Average score must meet overall threshold
    """

    if USE_ALL_MUST_PASS:
        # All categories must pass
        for category, details in scores.items():
            if category not in rubric:
                continue
            if details['score'] < rubric[category]['pass_threshold']:
                return False
        return True
    else:
        # Weighted average must pass
        weighted_sum = 0
        total_weight = 0

        for category, details in scores.items():
            if category not in rubric:
                continue
            weight = rubric[category].get('weight', 1.0)
            weighted_sum += details['score'] * weight
            total_weight += weight

        weighted_avg = weighted_sum / total_weight if total_weight > 0 else 0
        return weighted_avg >= OVERALL_THRESHOLD

