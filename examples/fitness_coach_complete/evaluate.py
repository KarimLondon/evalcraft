def create_judge_prompt(query: str, response: str, rubric: Dict[str, Any]) -> str:
    """
    Generate LLM-as-judge prompt with rubric and reference examples.

    Best practices embedded:
    - Include clear scoring criteria
    - Provide reference examples
    - Request chain-of-thought reasoning
    - Ask for structured output
    """

    prompt = f"""You are an expert evaluator for AI agent outputs.

Evaluate the following response using these criteria:

"""

    # Add categories with scales
    for i, (category, details) in enumerate(rubric.items(), 1):
        prompt += f"{i}. **{category.title()}** (1-5): {details['description']}\n"
        prompt += f"   Pass threshold: ≥{details['pass_threshold']}\n\n"

        # Add scale definitions
        prompt += "   Scale:\n"
        for score in [5, 4, 3, 2, 1]:
            if score in details['scale']:
                prompt += f"   - {score}: {details['scale'][score]}\n"
        prompt += "\n"

    # Add reference examples
    prompt += "**Reference Examples:**\n\n"
    for category, details in rubric.items():
        if 'reference_examples' in details:
            for score, example in details['reference_examples'].items():
                prompt += f"[{category.title()} - Score {score}]: \"{example}\"\n"
    prompt += "\n"

    # Add query and response
    prompt += f"**User Query:**\n{query}\n\n"
    prompt += f"**Assistant Response:**\n{response}\n\n"

    # Request structured output with reasoning
    prompt += """**Instructions:**
1. First, explain your reasoning for each criterion
2. Then, provide scores in JSON format

Provide your evaluation as:

**Reasoning:**
[Your analysis for each criterion]

**Scores:**
```json
{
  "category_name": {
    "score": <1-5>,
    "reasoning": "<brief explanation>"
  },
  ...
}
```
"""

    return prompt

