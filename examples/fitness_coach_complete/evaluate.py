def print_summary(metrics: Dict[str, Any]):
    """Print evaluation summary to console."""

    print("\n" + "="*60)
    print("EVALUATION SUMMARY")
    print("="*60)

    print(f"\n📊 Overall Results:")
    print(f"   Pass Rate: {metrics['overall_pass_rate']:.1%} ({metrics['overall_pass_count']}/{metrics['total_cases']} cases)")

    pass_emoji = "✅" if metrics['overall_pass_rate'] >= 0.7 else "⚠️"
    print(f"   Status: {pass_emoji}")

    print(f"\n📈 Per-Category Scores:")
    for category, cat_metrics in metrics['categories'].items():
        status = "✅" if cat_metrics['pass_rate'] >= 0.7 else "⚠️"
        print(f"   {status} {category.title()}: {cat_metrics['mean']:.1f}/5.0 (pass rate: {cat_metrics['pass_rate']:.1%})")

    print(f"\n💡 Recommendations:")
    if metrics['overall_pass_rate'] >= 0.9:
        print(f"   • Pass rate is very high ({metrics['overall_pass_rate']:.1%}) - consider if evals are challenging enough")
    elif metrics['overall_pass_rate'] < 0.5:
        print(f"   • Pass rate is low ({metrics['overall_pass_rate']:.1%}) - review flagged_failures.csv for patterns")

    lowest_cat = min(metrics['categories'].items(), key=lambda x: x[1]['mean'])
    print(f"   • Focus on improving: {lowest_cat[0].title()} (lowest score: {lowest_cat[1]['mean']:.1f}/5.0)")

    print(f"\n" + "="*60 + "\n")

