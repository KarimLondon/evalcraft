def save_results(
    results: List[Dict[str, Any]],
    metrics: Dict[str, Any],
    run_dir: Path,
    rubric: Dict[str, Any]
):
    """
    Save results in multiple formats:
    - scores.json: Structured results
    - flagged_failures.csv: Cases that failed evaluation
    - report.html: Interactive HTML report
    """

    # Save structured JSON
    with open(run_dir / "scores.json", "w") as f:
        json.dump({
            "project": PROJECT_NAME,
            "timestamp": datetime.now().isoformat(),
            "metrics": metrics,
            "results": results
        }, f, indent=2)

    # Save flagged failures
    failures = []
    for result in results:
        if not result['success']:
            failures.append(result)
            continue

        if not check_overall_pass(result['scores'], rubric):
            failures.append(result)

    if failures:
        failure_rows = []
        for f in failures:
            row = {
                "test_case_id": f['test_case_id'],
                "query": f['query'],
                "response": f['response']
            }
            for cat, details in f['scores'].items():
                row[f"{cat}_score"] = details['score']
            failure_rows.append(row)

        df_failures = pd.DataFrame(failure_rows)
        df_failures.to_csv(run_dir / "flagged_failures.csv", index=False)

    # Generate HTML report
    generate_html_report(results, metrics, run_dir, rubric)

    print(f"📁 Results saved to: {run_dir}/")
    print(f"   - scores.json (structured results)")
    print(f"   - flagged_failures.csv ({len(failures)} cases)")
    print(f"   - report.html (interactive report)")

